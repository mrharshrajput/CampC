// Get the div element by its id
let div = document.getElementById("myDiv");

// Get the content of the div element using innerHTML
let html = div.innerHTML;

// Get the content of the div element using innerText
let text = div.innerText;

// Display the content on the console
console.log("The content using innerHTML is: " + html);
console.log("The content using innerText is: " + text);
